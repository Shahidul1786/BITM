package com.example.bitmd11RecyclerViewuserInputitemClick;

public class User {

   String name;
   String phoneNo;
   String email;

    public User() {
    }

    public User(String name, String phoneNo, String email) {
        this.name = name;
        this.phoneNo = phoneNo;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public String getPhoneNo() {
        return phoneNo;
    }

    public String getEmail() {
        return email;
    }
}

